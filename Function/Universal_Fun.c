void readAndUpdateSystemDateTimeFun(void)
{
	DateTime * dateTime = NULL;
	
	dateTime = MyMalloc(DateTimeStructSize);
	if(dateTime)
	{
		if(My_Pass == RTC_GetTimeData(dateTime))
			setSystemTime(dateTime);
	}
	
	MyFree(dateTime);
}
