/***************************************************************************************************
*FunctionName：GetLCDInputData
*Description：获取LCD按键输入数据
*Input：None
*Output：None
*Author：xsx
*Data：2016年4月28日17:43:24
***************************************************************************************************/
void LCDInputData(void)
{
	ReceiveDataFromQueue(GetUsart6RXQueue(), GetUsart6RxMutex(), buf, 100, &rxcount, 1, 10 / portTICK_RATE_MS, 10 / portTICK_RATE_MS);
	
	if(rxcount > 0)
		AnalysisCode(buf, rxcount);
}

/***************************************************************************************************
*FunctionName：AnalysisCode
*Description：处理接收的数据
*Input：None
*Output：None
*Author：xsx
*Data：2016年4月29日10:08:39
***************************************************************************************************/
static void AnalysisCode(unsigned char * pbuf, unsigned short len)
{
	if(pbuf[0] == LCD_Head_1 && pbuf[1] == LCD_Head_2 && len == (pbuf[2]+3))
	{
		activityInputFunction(pbuf, len);
	}
}
