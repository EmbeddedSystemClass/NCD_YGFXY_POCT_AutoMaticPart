/***************************************************************************************************
*FunctionName: createIntent
*Description: 创建消息
*Input: 
*Output: 
*Return: 
*Author: xsx
*Date: 2016年12月21日14:21:12
***************************************************************************************************/
Intent * createIntent(void * data, unsigned short datalen)
{
	Intent * intent = NULL;
	
	if(NULL == data)
		return NULL;
	
	intent = MyMalloc(sizeof(Intent));
	
	if(intent)
	{
		memset(intent, 0, sizeof(Intent));
		
		intent->data = MyMalloc(datalen);
		intent->datalen = datalen;
		
		if(intent->data)
			memcpy(intent->data, data, datalen);

		else
		{
			MyFree(intent);
			intent = NULL;
		}
	}
	
	return intent;
}

/***************************************************************************************************
*FunctionName: readIntent
*Description: 读取消息
*Input: 
*Output: 
*Return: 
*Author: xsx
*Date: 2016年12月21日14:24:39
***************************************************************************************************/
void readIntent(Intent * intent, void * data)
{
	if((NULL == intent) || (NULL == data))
		return;
	
	memcpy(data, intent->data, intent->datalen);
	
	MyFree(intent->data);
	MyFree(intent);
}
