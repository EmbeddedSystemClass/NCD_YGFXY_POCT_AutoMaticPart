unsigned char CheckSumFun(void * data, unsigned short dataLen, unsigned char * sum)
{
	unsigned int acc = 0;
	unsigned char * dataP = (unsigned char * )data;
	
	while(dataLen--)
	{
		acc += *dataP++;
	}
   
	acc = (acc&0xffUL);
	
	if(sum)
		*sum = acc;
	
	return acc;
}

