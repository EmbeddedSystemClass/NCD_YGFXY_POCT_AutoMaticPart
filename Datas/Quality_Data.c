MyRes CreateADeviceQuality(void)
{
	if(GB_DeviceQuality == NULL)
		GB_DeviceQuality = MyMalloc(DeviceQualityStructSize);
	
	if(GB_DeviceQuality)
	{
		memset(GB_DeviceQuality, 0, DeviceQualityStructSize);
		
		return My_Pass;
	}
	
	return My_Fail;
}

DeviceQuality * getGB_DeviceQuality(void)
{
	return GB_DeviceQuality;
}

Operator * getDeviceQualityOperator(void)
{
	return &GB_DeviceQuality->operator;
}

void deleteGB_DeviceQuality(void)
{
	MyFree(GB_DeviceQuality);
	GB_DeviceQuality = NULL;
}
