char StartMotorTask(void)
{
	MotorActionInit();
	
	return xTaskCreate( vMotorTask, MotorTaskName, configMINIMAL_STACK_SIZE, NULL, vMotorTask_PRIORITY, NULL );
}

/************************************************************************
** 函数名:
** 功  能:
** 输  入:无
** 输  出:无
** 返  回：无
** 备  注：无
** 时  间:  
** 作  者：xsx                                                 
************************************************************************/
static void vMotorTask( void *pvParameters )
{
	while(1)
	{
		MotorActionFunction();	
	}
}




