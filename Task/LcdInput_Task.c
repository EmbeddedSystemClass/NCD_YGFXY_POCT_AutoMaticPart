/***************************************************************************************************
*FunctionName：StartvSysLedTask
*Description：建立系统指示灯任务
*Input：None
*Output：None
*Author：xsx
*Data：2015年8月26日16:58:13
***************************************************************************************************/
char StartvLcdInputTask(void)
{
	return xTaskCreate( vLcdInputTask, LcdInputTaskName, configMINIMAL_STACK_SIZE*2, NULL, LcdInputTask_PRIORITY, NULL );
}

/***************************************************************************************************
*FunctionName：vSysLedTask
*Description：系统指示灯闪烁表面程序正常运行
*Input：None
*Output：None
*Author：xsx
*Data：2015年8月26日16:58:46
***************************************************************************************************/
static void vLcdInputTask( void *pvParameters )
{
	while(1)
	{
		LCDInputData();
	}
}
