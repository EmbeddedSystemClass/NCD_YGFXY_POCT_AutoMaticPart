#define LwipComTask_PRIORITY			2			//看门狗任务优先级
const char * LwipComTaskName = "vLwipComTask";		//看门狗任务名
static struct netif * tempnetif = NULL;
/***************************************************************************************************/
/**************************************局部函数声明*************************************************/
